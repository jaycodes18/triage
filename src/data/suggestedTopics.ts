export function matchSuggestedTopics(subject: string): string[] {
  if (!subject) return [];
  const raw = subject.trim().toLowerCase();
  if (!raw) return [];

  // Exact canonical match
  for (const key of Object.keys(suggestedTopics)) {
    if (key.toLowerCase() === raw) return suggestedTopics[key];
  }
  // Alias match
  if (aliases[raw]) return suggestedTopics[aliases[raw]];

  // Substring match against canonical keys and alias keys
  for (const key of Object.keys(suggestedTopics)) {
    if (key.toLowerCase().includes(raw) || raw.includes(key.toLowerCase())) {
      return suggestedTopics[key];
    }
  }
  for (const alias of Object.keys(aliases)) {
    if (raw.includes(alias) || alias.includes(raw)) {
      return suggestedTopics[aliases[alias]];
    }
  }
  return [];
}
